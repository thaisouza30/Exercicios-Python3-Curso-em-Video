a = input('Digite algo:')
print('O tipo primitivo desse valor é', type(a))
print('Só tem espaços?',a.isspace())
print('É numérico?',a.isnumeric())
print('Contem letra e números?',a.isalnum())
print('É alfabético?',a.isalpha())
print('Esta todo em letras maiúsculas?',a.isupper())
print('É um valor vazio?',a.isdigit())
print('Esta em minúsculas?',a.islower())
print('É um decimal?',a.isdecimal())







